def material_names() -> List[str]:
    """Return all material identifiers available in the catalog."""
    return sorted(MATERIAL_DATABASE.keys())

def recyclable_materials() -> Dict[str, MaterialRecord]:
    """Return a filtered view of recyclable materials."""
    return {name: record for name, record in MATERIAL_DATABASE.items() if record['recyclable']}

def category_index() -> Dict[str, List[str]]:
    """Return mapping from category to material identifiers."""
    index: Dict[str, List[str]] = {}
    for name, record in MATERIAL_DATABASE.items():
        index.setdefault(record['category'], []).append(name)
    for members in index.values():
        members.sort()
    return index
